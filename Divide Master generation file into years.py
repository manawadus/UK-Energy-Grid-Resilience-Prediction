import pandas as pd
from google.colab import files

# 1. Upload full generation CSV (2009–2025)
uploaded = files.upload()
filename = next(iter(uploaded))

# 2. Read the CSV
df = pd.read_csv(filename)

# 3. Convert DATETIME column to proper datetime
df["DATETIME"] = pd.to_datetime(df["DATETIME"])

# 4. Keep only rows from 2020-01-01 to end of 2025-09
start_date = "2020-01-01"
end_date_exclusive = "2025-10-01"   # < 1st Oct 2025 → includes all of 2025-09

mask = (df["DATETIME"] >= start_date) & (df["DATETIME"] < end_date_exclusive)
df_2020_2025 = df.loc[mask].copy()

# 5. Create a YEAR column (used only for splitting)
df_2020_2025["YEAR"] = df_2020_2025["DATETIME"].dt.year

# 6. Save one CSV per year (2020, 2021, 2022, 2023, 2024, 2025)
for year, group in df_2020_2025.groupby("YEAR"):
    out_name = f"generation_{year}.csv"
    # drop YEAR helper column to keep only original columns
    group.drop(columns=["YEAR"]).to_csv(out_name, index=False)
    print(f"Saved: {out_name}")
    files.download(out_name)