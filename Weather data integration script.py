# ===== Upload + Align Hourly Weather to 30-min Calendar from Generation (YEAR as input) =====
import pandas as pd
import numpy as np
from google.colab import files
import io

pd.set_option("display.max_columns", 200)
pd.set_option("display.width", 160)

# -----------------------
# 0) Ask user for YEAR
# -----------------------
year_str = input("Enter year to process (e.g. 2023): ").strip()
YEAR = int(year_str)  # simple: assume user enters a valid year like 2023

start_date = f"{YEAR}-01-01"
end_date   = f"{YEAR + 1}-01-01"

# -----------------------
# 1) Upload files
# -----------------------
print("Upload your 30-min master generation file (CSV/XLSX, must contain 'DATETIME' in UTC)...")
gen_upload = files.upload()
gen_filename = list(gen_upload.keys())[0]

print("Upload your hourly weather (wide) file (CSV/XLSX, must contain 'Datetime' in UTC)...")
wx_upload = files.upload()
wx_filename = list(wx_upload.keys())[0]

# -----------------------
# 2) Read files (robust to encoding if CSV)
# -----------------------
def read_any(upload_dict, fname):
    bio = io.BytesIO(upload_dict[fname])
    if fname.lower().endswith((".xlsx", ".xls")):
        return pd.read_excel(bio)
    try:
        return pd.read_csv(bio, low_memory=False, encoding="utf-8")
    except UnicodeDecodeError:
        bio.seek(0)
        return pd.read_csv(bio, low_memory=False, encoding="ISO-8859-1")

gen = read_any(gen_upload, gen_filename)
wx  = read_any(wx_upload, wx_filename)

# -----------------------
# 3) Parse timestamps as UTC (do not rename columns)
# -----------------------
if "DATETIME" not in gen.columns:
    raise ValueError("Generation file must have a 'DATETIME' column.")
gen["DATETIME"] = pd.to_datetime(gen["DATETIME"], utc=True, errors="coerce")

if "Datetime" not in wx.columns:
    raise ValueError("Weather file must have a 'Datetime' column.")
wx["Datetime"] = pd.to_datetime(wx["Datetime"], utc=True, errors="coerce")

# -----------------------
# 4) Keep only selected YEAR
# -----------------------
gen_year = gen[(gen["DATETIME"] >= start_date) & (gen["DATETIME"] < end_date)].copy()
wx_year  = wx[(wx["Datetime"]  >= start_date) & (wx["Datetime"]  < end_date)].copy()

print(f"Gen {YEAR} rows (30-min):", len(gen_year))
print(f"Wx  {YEAR} rows (hourly):", len(wx_year))

# -----------------------
# 5) Build the exact 30-min calendar we want (from generation)
#    This guarantees '{YEAR}-12-31 23:30:00+00:00' exists if present in gen.
# -----------------------
full_30 = pd.DatetimeIndex(gen_year["DATETIME"].sort_values().unique(), name="Datetime")

# -----------------------
# 6) Prepare weather:
#    - Deduplicate
#    - Set index to 'Datetime'
#    - Resample to 30-min within existing range (creates :30 between hours)
#    - Reindex to full_30 (forces last 23:30 row), then ffill/bfill to fill gaps
# -----------------------
wx_year = wx_year.drop_duplicates(subset=["Datetime"]).copy()
wx_year = wx_year.set_index("Datetime").sort_index()

# Step 6a: upsample within existing weather range
wx_30 = wx_year.resample("30min").ffill()

# Step 6b: force the full calendar (now includes 23:30) and fill edges
wx_30 = wx_30.reindex(full_30).ffill().bfill().reset_index()  # 'Datetime' restored

# -----------------------
# 7) Merge (left) on generation timestamps and save
# -----------------------
merged = gen_year.merge(wx_30, left_on="DATETIME", right_on="Datetime", how="left").drop(columns=["Datetime"])

out_name = f"master_generation_{YEAR}_with_weather.csv"
merged.to_csv(out_name, index=False, encoding="utf-8-sig")

print(f"\nSaved: {out_name}  |  Rows: {len(merged)}  |  Cols: {len(merged.columns)}")

files.download(out_name)

# -----------------------
# 8) Quick QA checks
# -----------------------
# a) row count match
print("\n[QA] merged rows == gen_year rows:", len(merged) == len(gen_year))

# b) last two rows preview (should include 23:00 and 23:30 with weather filled, if present)
print("\n[QA] Tail timestamps:")
print(merged["DATETIME"].tail(2).to_string(index=False))

# c) count NaNs in the last row across all columns (should ideally be 0 or very small)
nans_last = merged.iloc[-1].isna().sum()
print("\n[QA] NaN count in last row:", nans_last)

# d) verify last timestamp is 23:30 UTC for the selected year (if the calendar covers full year)
expected_last = pd.Timestamp(f"{YEAR}-12-31 23:30:00", tz="UTC")
print("[QA] Last timestamp is 23:30 UTC on 31 Dec:",
      merged["DATETIME"].iloc[-1] == expected_last)