import pandas as pd
import numpy as np
from io import BytesIO
from google.colab import files

print("⬆️ Please upload the 2023 generation Excel file (.xlsx) ...")
gen_upload = files.upload()  # Expect exactly one file
gen_filename = next(iter(gen_upload.keys()))
print("✅ Received:", gen_filename)

print("⬆️ Please upload the monthly frequency CSV files for 2023 (dtm,f) ...")
freq_uploads = files.upload()  # Multiple files allowed
freq_filenames = list(freq_uploads.keys())
print(f"✅ Received {len(freq_filenames)} frequency files.")

# -----------------------
# CONFIG / CONSTANTS
# -----------------------
EXPECTED_PER_BIN = 60  # 30 minutes at 30-second sampling → 60 records
OUT_XLSX = "gen_sysfrequency.xlsx"
MERGED_SHEET = "merged_2023"
QA_SHEET = "QA_summary"

# -----------------------
# Load Generation (keep DATETIME text column unchanged)
# -----------------------
# Read Excel (DATETIME may be parsed as datetime or text depending on the file; we preserve the original column)
gen_raw = pd.read_excel(BytesIO(gen_upload[gen_filename]))

if "DATETIME" not in gen_raw.columns:
    raise ValueError("The generation file must contain a 'DATETIME' column.")

# Preserve original DATETIME text by creating a separate datetime key for merging
gen = gen_raw.copy()

# Create robust merge key as pandas datetime (UTC-based timestamps but tz-naive)
# If DATETIME is already datetime-like, convert; if it's string, parse.
gen["_DT_KEY"] = pd.to_datetime(gen["DATETIME"], errors="coerce", utc=False)

# If parsing failed (all NA), try a stricter parse
if gen["_DT_KEY"].isna().all():
    # common format 'YYYY-MM-DDTHH:MM:SS'
    gen["_DT_KEY"] = pd.to_datetime(gen["DATETIME"], format="%Y-%m-%dT%H:%M:%S", errors="coerce", utc=False)

if gen["_DT_KEY"].isna().any():
    bad = gen.loc[gen["_DT_KEY"].isna(), "DATETIME"].head(5).tolist()
    raise ValueError(f"Failed to parse some DATETIME values. Examples: {bad}")

# Ensure there are no duplicate timestamps on the generation side
gen = gen.drop_duplicates(subset=["_DT_KEY"]).sort_values("_DT_KEY").reset_index(drop=True)

# -----------------------
# Load & Concatenate Frequency CSVs
# Each has columns: dtm (UTC timestamp, 30s sampling), f (Hz)
# -----------------------
freq_parts = []
for fn in freq_filenames:
    # Use uploaded in-memory content
    content = BytesIO(freq_uploads[fn])
    df = pd.read_csv(content)
    if not {"dtm", "f"}.issubset(df.columns):
        raise ValueError(f"Frequency file '{fn}' must have columns ['dtm','f'].")

    # Parse timestamps (tz-naive; treated as UTC moments)
    df["dtm"] = pd.to_datetime(df["dtm"], errors="coerce", utc=False)
    df = df.dropna(subset=["dtm", "f"])
    freq_parts.append(df[["dtm", "f"]])

freq = pd.concat(freq_parts, ignore_index=True)

# De-duplicate and sort
freq = freq.drop_duplicates(subset=["dtm"]).sort_values("dtm").reset_index(drop=True)

# Limit to 2023 only (safety)
#freq = freq[(freq["dtm"] >= "2023-01-01") & (freq["dtm"] < "2024-01-01")].copy()

# -----------------------
# Bin to 30-minute intervals (left-closed bins at :00 and :30)
# -----------------------
# Compute bin start (use '30min' to avoid deprecation warning)
freq["bin_start"] = freq["dtm"].dt.floor("30min")

# Define strict out-of-band flags (counts are number of 30s samples, NOT seconds)
# (Kept inline in lambdas below)
agg = (
    freq.groupby("bin_start")
        .agg(
            freq_mean=("f", "mean"),
            freq_min=("f", "min"),
            freq_max=("f", "max"),
            freq_std=("f", "std"),
            out_of_49_5_50_5=("f", lambda s: np.count_nonzero((s < 49.5) | (s > 50.5))),
            out_of_49_8_50_2=("f", lambda s: np.count_nonzero((s < 49.8) | (s > 50.2))),
            n_points=("f", "size")
        )
        .reset_index()
)

# Round stats to 4 decimals (counts remain integers)
for col in ["freq_mean", "freq_min", "freq_max", "freq_std"]:
    agg[col] = agg[col].round(4)

# Create merge key without altering the generation file's DATETIME column
agg["_DT_KEY"] = agg["bin_start"]  # pandas datetime; aligns to gen["_DT_KEY"]

# -----------------------
# Merge
# -----------------------
merged = pd.merge(
    gen,                               # keeps original DATETIME column unchanged
    agg.drop(columns=["bin_start"]),   # we don't need bin_start in output
    on="_DT_KEY",
    how="left"
)

# Optional: Move the added columns next to DATETIME for readability
freq_cols = [
    "freq_mean", "freq_min", "freq_max", "freq_std",
    "out_of_49_5_50_5", "out_of_49_8_50_2", "n_points"
]
ordered_cols = (
    ["DATETIME"] +
    [c for c in merged.columns if c not in (["DATETIME"] + freq_cols)] +
    freq_cols
)
merged = merged[ordered_cols]

# -----------------------
# QA Summary Sheet
# -----------------------
# Expected: 48 half-hours per day in UTC
qa = {}

# Per-day bin counts (based on the frequency aggregation)
if not agg.empty:
    per_day = agg["_DT_KEY"].dt.date.value_counts().sort_index()
    qa["per_day_30min_bins"] = per_day

# Distribution of n_points (completeness)
if "n_points" in merged.columns:
    qa["n_points_summary"] = merged["n_points"].describe()

# Count of bins with missing frequency agg (no match)
qa["rows_with_no_freq"] = int(merged["freq_mean"].isna().sum())

# Convert QA items to DataFrame(s)
qa_frames = []
if "per_day_30min_bins" in qa:
    qa_frames.append((
        "per_day_bin_counts",
        qa["per_day_30min_bins"].rename("bin_count").reset_index(name="date")
    ))
if "n_points_summary" in qa:
    temp_df = qa["n_points_summary"].to_frame("value").reset_index()
    temp_df.columns = ["metric", "value"]
    qa_frames.append(("n_points_summary", temp_df))

qa_rows_no_freq = pd.DataFrame(
    {"metric": ["rows_with_no_freq"], "value": [qa["rows_with_no_freq"]]}
)
qa_frames.append(("missing_rows", qa_rows_no_freq))

# -----------------------
# Save to Excel (two sheets): merged_2023, QA_summary
# -----------------------
with pd.ExcelWriter(OUT_XLSX, engine="openpyxl") as writer:
    merged.to_excel(writer, index=False, sheet_name=MERGED_SHEET)
    startrow = 0
    for title, dfqa in qa_frames:
        dfqa.to_excel(writer, index=False, sheet_name=QA_SHEET, startrow=startrow)
        startrow += len(dfqa) + 3  # spacing

print(f"✅ Saved: {OUT_XLSX}")

