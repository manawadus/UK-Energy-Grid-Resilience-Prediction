import pandas as pd
import numpy as np
from google.colab import files

# ================================
# 1. Upload annual CSV files (2020–2025)
# ================================
print("Please upload all GB_master_YYYY.csv files for 2020–2025")
uploaded = files.upload()   # Select multiple files at once

dfs = []
for fname, content in uploaded.items():
    # Only process CSVs
    if not fname.lower().endswith(".csv"):
        print(f"Skipping non-CSV file: {fname}")
        continue

    print(f"Reading: {fname}")
    df = pd.read_csv(
        fname,
        parse_dates=["Datetime_utc"],   # Very important for time checks
        dayfirst=False
    )
    dfs.append(df)

if not dfs:
    raise RuntimeError("No CSV files were uploaded / read correctly.")

# ================================
# 2. Combine and sort
# ================================
combined = pd.concat(dfs, ignore_index=True)

# Sort by UTC timestamp
combined = combined.sort_values("Datetime_utc").reset_index(drop=True)

print(f"\nCombined rows: {len(combined)}")
print("Time range:",
      combined['Datetime_utc'].min(),
      "→",
      combined['Datetime_utc'].max())

# ================================
# 3. Detect duplicates (same Datetime_utc)
# ================================
dup_mask = combined.duplicated(subset=["Datetime_utc"], keep=False)
duplicates = combined.loc[dup_mask].copy()

print("\nNumber of duplicate timestamps:",
      duplicates['Datetime_utc'].nunique(),
      "(rows:", len(duplicates), ")")

# ================================
# 4. Detect gaps (missing 30-min steps)
# ================================
# Expected continuous 30-min series between first and last timestamps
start = combined["Datetime_utc"].min()
end   = combined["Datetime_utc"].max()

expected_index = pd.date_range(start=start, end=end, freq="30min")
actual_index = combined["Datetime_utc"]

# Missing timestamps = in expected_index but not in actual_index
missing_timestamps = expected_index.difference(actual_index)

print("Number of missing 30-min timestamps:", len(missing_timestamps))

# Put missing into a DataFrame for easier inspection / export
missing_df = pd.DataFrame({"Datetime_utc": missing_timestamps})

# ================================
# 5. Save outputs
# ================================
# Master combined file
out_master = "GB_master_2020_2025.csv"
combined.to_csv(out_master, index=False, encoding="utf-8-sig")
print(f"\nSaved combined file: {out_master}")

# Missing timestamps
out_missing = "missing_timestamps.csv"
missing_df.to_csv(out_missing, index=False)
print(f"Saved missing timestamps: {out_missing} (rows: {len(missing_df)})")

# Duplicate timestamps (if any)
if len(duplicates) > 0:
    out_dups = "duplicate_timestamps.csv"
    duplicates.to_csv(out_dups, index=False)
    print(f"Saved duplicate timestamps: {out_dups} (rows: {len(duplicates)})")
else:
    print("No duplicate timestamps detected.")

# Offer downloads
files.download(out_master)
files.download(out_missing)
if len(duplicates) > 0:
    files.download(out_dups)

# Optional: quick peek
display(combined.head(8))
display(combined.tail(8))
print("\nFirst 10 missing timestamps:")
display(missing_df.head(10))