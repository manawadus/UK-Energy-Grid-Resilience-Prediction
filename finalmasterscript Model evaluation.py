# =========================
# 1) Supply–Demand Margin (M)
#   margin_mw = Generation − Demand
#   M = clip( max(0, margin_mw − 0.02 × Demand) / ref , 0, 1 )
# =========================
margin_mw = generation - demand
M = np.clip(np.maximum(0.0, margin_mw - 0.02 * demand) / ref_capacity, 0.0, 1.0)

# =========================
# 2) Fuel-mix Diversity (D), inverse Herfindahl
#   HHI = Σ_f (s_f^2), s_f = GEN_f / Generation_from_fuels
#   D = clip(1 − HHI, 0, 1)
# =========================
fuel_cols = ["GAS","COAL","NUCLEAR","HYDRO","IMPORTS","BIOMASS","OTHER","SOLAR","STORAGE","WIND_TOTAL"]
fuel_cols = [c for c in fuel_cols if c in df_gri.columns]

fuel_mat   = df_gri[fuel_cols].astype(float)
fuel_total = fuel_mat.sum(axis=1)
# Avoid division by zero: if fuel_total==0 → shares=0
shares = fuel_mat.div(fuel_total.replace(0, np.nan), axis=0).fillna(0.0)
HHI = (shares**2).sum(axis=1)
D = np.clip(1.0 - HHI, 0.0, 1.0)

# =========================
# 3) Import Dependence (I)
#   imports_share = IMPORTS / GENERATION
#   I = clip( 1 − (imports_share − 0.05)/(0.40 − 0.05), 0, 1 )
# =========================
imports = df_gri["IMPORTS"].astype(float) if "IMPORTS" in df_gri.columns else 0.0
imports_share = np.where(generation > 0, imports / generation, 0.0)
I = np.clip(1.0 - (imports_share - 0.05) / (0.40 - 0.05), 0.0, 1.0)

# =========================
# 4) Storage Contribution (S)
#   S = clip( max(0, STORAGE)/Demand / 0.05 , 0, 1 )
# =========================
storage = df_gri["STORAGE"].astype(float) if "STORAGE" in df_gri.columns else 0.0
S = np.clip(np.where(demand > 0, np.maximum(0.0, storage) / demand / 0.05, 0.0), 0.0, 1.0)

# =========================
# 5) Frequency Stability (F)
#   From 1-s frequency metrics:
#   F = clip( 1 − (|50 − f_mean| − 0.10)/(0.50 − 0.10), 0, 1 )
# =========================
fmean = df_gri["freq_mean"].astype(float)
delta = np.abs(50.0 - fmean)
F = np.clip(1.0 - (delta - 0.10) / (0.50 - 0.10), 0.0, 1.0)

# =========================
# 6) Weighted GRI (0–100)
#   GRI = 100 × (0.40*M + 0.20*D + 0.10*I + 0.10*S + 0.20*F)
# =========================
wM, wD, wI, wS, wF = 0.40, 0.20, 0.10, 0.10, 0.20
GRI = 100.0 * (wM*M + wD*D + wI*I + wS*S + wF*F)

# Attach to dataframe
df_gri["M"]   = M
df_gri["D"]   = D
df_gri["I"]   = I
df_gri["S"]   = S
df_gri["F"]   = F
df_gri["GRI"] = GRI

