# ------- 2) PCA helper -------
def pca_for_group(df, cols, prefix, var_threshold=0.95):
    """
    Returns:
      df_pca  - DataFrame of components with names like f"{prefix}_PCA_1"
      model   - fitted PCA model
      evr     - explained_variance_ratio_ (array)
    """
    if not cols:
        return pd.DataFrame(index=df.index), None, np.array([])

    X = df[cols].copy().fillna(0.0)  # assume you've already handled missing/outliers
    X_scaled = StandardScaler(with_mean=True, with_std=True).fit_transform(X)

    pca = PCA(n_components=var_threshold, svd_solver="full")
    X_pca = pca.fit_transform(X_scaled)

    comp_cols = [f"{prefix}_PCA_{i+1}" for i in range(X_pca.shape[1])]
    df_pca = pd.DataFrame(X_pca, columns=comp_cols, index=df.index)
    return df_pca, pca, pca.explained_variance_ratio_

