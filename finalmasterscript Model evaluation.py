def _safe_cols(df, cols):
    """Return only those columns that actually exist in df."""
    return [c for c in cols if c in df.columns]


def add_lags(df, lag_map):
    """Add lag features based on a {column: [lags]} mapping."""
    out = df.copy()
    for col, lags in lag_map.items():
        if col not in out.columns:
            continue
        for l in lags:
            out[f"{col}_lag{l}"] = out[col].shift(l)
    return out


def add_rolling(df, cols, windows):
    """Add rolling mean/std for given columns and window sizes."""
    out = df.copy()
    cols = _safe_cols(out, cols)
    for col in cols:
        for w in windows:
            out[f"{col}_roll{w}_mean"] = out[col].rolling(window=w, min_periods=w).mean()
            out[f"{col}_roll{w}_std"]  = out[col].rolling(window=w, min_periods=w).std()
    return out


def add_calendar(df, datetime_col="DATETIME"):
    """
    Add calendar & seasonal features for multi-year time series:
      - year, month, dayofyear, dayofweek, is_weekend
      - hour
      - cyclic encodings: hour_sin/cos, month_sin/cos
    """
    out = df.copy()
    if datetime_col not in out.columns:
        return out

    dt = pd.to_datetime(out[datetime_col], utc=True, errors="coerce")
    out[datetime_col] = dt

    # Basic calendar features
    out["year"]       = dt.dt.year
    out["month"]      = dt.dt.month
    out["dayofyear"]  = dt.dt.dayofyear
    out["dayofweek"]  = dt.dt.dayofweek    # 0=Mon ... 6=Sun
    out["hour"]       = dt.dt.hour
    out["is_weekend"] = (out["dayofweek"] >= 5).astype(int)

    # Cyclical encodings (seasonality and daily cycle)
    out["hour_sin"]  = np.sin(2 * np.pi * out["hour"] / 24.0)
    out["hour_cos"]  = np.cos(2 * np.pi * out["hour"] / 24.0)
    out["month_sin"] = np.sin(2 * np.pi * out["month"] / 12.0)
    out["month_cos"] = np.cos(2 * np.pi * out["month"] / 12.0)

    return out


