def safe_name(s: str) -> str:
    # Replace any character that is not alphanumeric, underscore, hyphen, or dot with an underscore
    s = re.sub(r"[^A-Za-z0-9_.-]+", "_", s)
    # Replace any remaining characters that are not alphanumeric or underscore with an underscore
    s = re.sub(r"[^A-Za-z0-9_]+", "_", s)
    # Remove leading/trailing underscores
    s = s.strip("_")
    # Limit length to avoid issues
    return s[:120]

