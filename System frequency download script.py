import os
import requests
from urllib.parse import urlparse
from google.colab import files
import re
import calendar


# ==========================
# CONFIG
# ==========================
API_URL = "https://api.neso.energy/api/3/action/datapackage_show"
DATASET_ID = "system-frequency-data"
OUT_DIR = "system_frequency_2020_2025"
os.makedirs(OUT_DIR, exist_ok=True)

# Desired date range
START_YEAR = 2020
END_YEAR = 2025
END_MONTH = 9  # September 2025

# ==========================
# 1. Load all resource metadata
# ==========================
params = {"id": DATASET_ID}
resp = requests.get(API_URL, params=params)
resp.raise_for_status()
resources = resp.json()["result"]["resources"]

print(f"Total resources in dataset: {len(resources)}")


def extract_year_month(title: str):
    """
    Try extracting year and month from resource title.
    Works for NESO monthly frequency dataset.
    """
    # Pattern 1: MonthName Year
    m = re.search(r"([A-Za-z]+)\s+(\d{4})", title)
    if m:
        month_name = m.group(1)
        year = int(m.group(2))
        try:
            month = list(calendar.month_name).index(month_name)
            return year, month
        except:
            pass

    # Pattern 2: YYYY-MM in filenames
    m = re.search(r"(\d{4})-(\d{2})", title)
    if m:
        return int(m.group(1)), int(m.group(2))

    return None, None

# ==========================
# 2. Filter resources within Jan 2020 – Sep 2025
# ==========================
selected = []
for r in resources:
    if r.get("format", "").upper() != "CSV":
        continue

    title = r.get("title", "")
    year, month = extract_year_month(title)

    if year is None or month is None:
        continue

    if (year > START_YEAR or (year == START_YEAR and month >= 1)) and \
       (year < END_YEAR or (year == END_YEAR and month <= END_MONTH)):
        selected.append((r, year, month))

# Sort chronologically
selected = sorted(selected, key=lambda x: (x[1], x[2]))

print(f"Resources in range Jan {START_YEAR} – Sep {END_YEAR}: {len(selected)}")

# ==========================
# 3. Download files
# ==========================
for r, year, month in selected:
    url = r["path"]
    filename = os.path.basename(urlparse(url).path)
    out_path = os.path.join(OUT_DIR, filename)

    if os.path.exists(out_path):
        print(f"Already exists, skipping: {filename}")
        continue

    print(f"Downloading {calendar.month_name[month]} {year} -> {filename}")
    file_resp = requests.get(url, stream=True)
    file_resp.raise_for_status()

    with open(out_path, "wb") as f:
        for chunk in file_resp.iter_content(chunk_size=8192):
            if chunk:
                f.write(chunk)

print("Download complete for Jan 2020 – Sep 2025 system frequency files.")


folder = "/content/system_frequency_2020_2025"
for file in os.listdir(folder):
    files.download(os.path.join(folder, file))