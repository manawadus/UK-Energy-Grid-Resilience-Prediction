import requests
import pandas as pd
from datetime import date, timedelta
import holidays
import pytz

# ---- Your original city dict (unchanged) ----
cities = {
    "Aberdeen": {"lat": 57.150, "lon": -2.094},       # Northern Scotland – onshore & offshore wind zone
    "Humber": {"lat": 53.768, "lon": -0.327},         # North Sea offshore wind hub (Dogger Bank, Hornsea)
    "Camborne": {"lat": 50.213, "lon": -5.297},       # South-West England – solar-rich, Atlantic influence
    "Ashford": {"lat": 51.148, "lon": 0.873},         # South-East England – high irradiance near demand centres
    "EastAnglia": {"lat": 52.205, "lon": 0.119}       # Eastern England – continental solar & easterly wind regimes
}

# ---- Your original date logic (unchanged) ----
end_date = date.today()
start_date = end_date - timedelta(days=2150)

# ---- Your original params (unchanged names/formatting) ----
params = {
    "hourly": [
        "temperature_2m",
        "precipitation",
        "windspeed_10m",
        "winddirection_10m",
        "shortwave_radiation",
        "cloudcover"
    ],
    "start_date": start_date.strftime("%Y-%m-%d"),
    "end_date": end_date.strftime("%Y-%m-%d"),
    "timezone": "GMT"
}

# Helper: fetch one city and return a wide (suffixed) dataframe
def fetch_city_as_wide(city_name, lat, lon):
    url = f"https://archive-api.open-meteo.com/v1/archive?latitude={lat}&longitude={lon}"
    r = requests.get(url, params=params)
    r.raise_for_status()
    data = r.json()
    if "hourly" not in data or not data["hourly"]:
        raise ValueError(f"No hourly data for {city_name}")

    df = pd.DataFrame(data["hourly"])

    # ---- Keep your original renames (unchanged) ----
    df.rename(columns={
        "temperature_2m": "Temperature (°C)",
        "precipitation": "Precipitation (mm)",
        "windspeed_10m": "Wind Speed (m/s)",
        "winddirection_10m": "Wind Direction (deg)",
        "shortwave_radiation": "Solar Irradiance (W/m²)",
        "cloudcover": "Cloud Cover (%)",
        "time": "Datetime"
    }, inplace=True)

    # Add city column (as you had), then pivot to wide by suffixing columns (except Datetime)
    df["city"] = city_name
    # Ensure Datetime is parsed uniformly
    df["Datetime"] = pd.to_datetime(df["Datetime"], utc=True)

    # Suffix columns with _<city in lowercase>, keep Datetime untouched
    suffix = "_" + city_name.lower()
    cols_to_suffix = [c for c in df.columns if c not in ["Datetime", "city"]]
    df.rename(columns={c: f"{c}{suffix}" for c in cols_to_suffix}, inplace=True)

    # Keep only Datetime + suffixed columns for merging
    keep_cols = ["Datetime"] + [c for c in df.columns if c.endswith(suffix)]
    return df[keep_cols].sort_values("Datetime")

# ---- Fetch & merge all cities on Datetime ----
wide = None
for city, coords in cities.items():
    part = fetch_city_as_wide(city, coords["lat"], coords["lon"])
    wide = part if wide is None else wide.merge(part, on="Datetime", how="outer")

# Sort by Datetime and save (BOM so Excel shows ° correctly; this does not change your column names)
wide = wide.sort_values("Datetime")

# ---------------- add Day & Holiday columns ----------------
# Ensure Datetime column is timezone-aware UTC
wide["Datetime"] = pd.to_datetime(wide["Datetime"], utc=True, errors="coerce")

# Convert to UK local time for weekday/holiday logic
uk_tz = pytz.timezone("Europe/London")
dt_local = wide["Datetime"].dt.tz_convert(uk_tz)

# Add weekday name
wide["Day"] = dt_local.dt.day_name()

# Build GB-wide holiday list (England, Wales, Scotland)
years = range(int(dt_local.dt.year.min()), int(dt_local.dt.year.max()) + 1)
hol_engw = holidays.UnitedKingdom(years=years, subdiv="England")
hol_wal  = holidays.UnitedKingdom(years=years, subdiv="Wales")
hol_sco  = holidays.UnitedKingdom(years=years, subdiv="Scotland")

gb_holidays_dates = set(hol_engw) | set(hol_wal) | set(hol_sco)

# Flag holiday Yes/No
local_dates = dt_local.dt.date
wide["Holiday"] = ["Yes" if d in gb_holidays_dates else "No" for d in local_dates]

