# === Build GB_master_<year>.csv for any year (2020–2025) ===
import numpy as np
import pandas as pd
from google.colab import files
import io

# ---------------- 0) Ask user for year ----------------
year = int(input("Enter year to process (2020–2025): ").strip())
if year < 2020 or year > 2025:
    raise ValueError("Year must be between 2020 and 2025.")

print(f"\nProcessing year: {year}\n")

# ---------------- 1) Upload generation file ----------------
print(f"Upload generation file for {year}")
gen_upload = files.upload()
gen_fname = next(iter(gen_upload.keys()))

# Auto-detect CSV or Excel
if gen_fname.endswith(".csv"):
    gen = pd.read_csv(io.BytesIO(gen_upload[gen_fname]))
else:
    gen = pd.read_excel(io.BytesIO(gen_upload[gen_fname]))

# ---------------- 2) Upload demand CSV file ----------------
print(f"\nUpload demand CSV for {year}")
dem_upload = files.upload()
dem_fname = next(iter(dem_upload.keys()))
dem = pd.read_csv(io.BytesIO(dem_upload[dem_fname]))

# ---------------- 3) Generation: create UTC + local keys ----------------
gen['Datetime_utc'] = pd.to_datetime(gen['DATETIME'], utc=True, errors='coerce')
gen = gen.sort_values('Datetime_utc').reset_index(drop=True)

gen['Datetime_local'] = gen['Datetime_utc'].dt.tz_convert('Europe/London')
gen['SETTLEMENT_DATE'] = gen['Datetime_local'].dt.strftime('%d-%b-%y')

gen = gen.sort_values('Datetime_local')
gen['SETTLEMENT_PERIOD'] = (
    gen.groupby(gen['Datetime_local'].dt.date).cumcount() + 1
).astype(int)

# ---------------- 4) Select required generation columns ----------------
required_cols = [
    "DATETIME",
    "GAS","COAL","NUCLEAR","WIND","WIND_EMB","HYDRO","IMPORTS","BIOMASS","OTHER","SOLAR","STORAGE",
    "GENERATION","CARBON_INTENSITY",
    "freq_mean","freq_min","freq_max","freq_std",
    "out_of_49_5_50_5","out_of_49_8_50_2","n_points",
    "Temperature (°C)_aberdeen","Precipitation (mm)_aberdeen",
    "Wind Speed (m/s)_aberdeen","Wind Direction (deg)_aberdeen",
    "Solar Irradiance (W/m²)_aberdeen","Cloud Cover (%)_aberdeen",
    "Temperature (°C)_humber","Precipitation (mm)_humber",
    "Wind Speed (m/s)_humber","Wind Direction (deg)_humber",
    "Solar Irradiance (W/m²)_humber","Cloud Cover (%)_humber",
    "Temperature (°C)_camborne","Precipitation (mm)_camborne",
    "Wind Speed (m/s)_camborne","Wind Direction (deg)_camborne",
    "Solar Irradiance (W/m²)_camborne","Cloud Cover (%)_camborne",
    "Temperature (°C)_ashford","Precipitation (mm)_ashford",
    "Wind Speed (m/s)_ashford","Wind Direction (deg)_ashford",
    "Solar Irradiance (W/m²)_ashford","Cloud Cover (%)_ashford",
    "Temperature (°C)_eastanglia","Precipitation (mm)_eastanglia",
    "Wind Speed (m/s)_eastanglia","Wind Direction (deg)_eastanglia",
    "Solar Irradiance (W/m²)_eastanglia","Cloud Cover (%)_eastanglia",
    "Day","Holiday"
]

keep_gen = (
    ['Datetime_utc','SETTLEMENT_DATE','SETTLEMENT_PERIOD'] +
    [c for c in required_cols if c in gen.columns]
)
gen_trim = gen[keep_gen].copy()

# ---------------- 5) Demand parsing (mm/dd/yyyy style like 1/10/2025) ----------------
dem['SETTLEMENT_DATE'] = pd.to_datetime(
    dem['SETTLEMENT_DATE'],
    dayfirst=False,        # IMPORTANT: 1/10/2025 = 10-Jan-2025
    errors='coerce'
).dt.strftime('%d-%b-%y')

dem['SETTLEMENT_PERIOD'] = (
    pd.to_numeric(dem['SETTLEMENT_PERIOD'], errors='coerce')
    .astype('Int64')
    .astype(int)
)

dem_trim = dem[['SETTLEMENT_DATE','SETTLEMENT_PERIOD','ND','TSD']].copy()

# ---------------- 6) Merge ----------------
master = (
    gen_trim
    .merge(dem_trim, on=['SETTLEMENT_DATE','SETTLEMENT_PERIOD'], how='left')
    .sort_values('Datetime_utc')
    .reset_index(drop=True)
)

# ---------------- 7) Final column order (exactly like your old files) ----------------
final_cols = [
    "DATETIME",
    "GAS","COAL","NUCLEAR","WIND","WIND_EMB","HYDRO","IMPORTS","BIOMASS","OTHER","SOLAR","STORAGE",
    "GENERATION","CARBON_INTENSITY",
    "freq_mean","freq_min","freq_max","freq_std",
    "out_of_49_5_50_5","out_of_49_8_50_2","n_points",
    "Temperature (°C)_aberdeen","Precipitation (mm)_aberdeen",
    "Wind Speed (m/s)_aberdeen","Wind Direction (deg)_aberdeen",
    "Solar Irradiance (W/m²)_aberdeen","Cloud Cover (%)_aberdeen",
    "Temperature (°C)_humber","Precipitation (mm)_humber",
    "Wind Speed (m/s)_humber","Wind Direction (deg)_humber",
    "Solar Irradiance (W/m²)_humber","Cloud Cover (%)_humber",
    "Temperature (°C)_camborne","Precipitation (mm)_camborne",
    "Wind Speed (m/s)_camborne","Wind Direction (deg)_camborne",
    "Solar Irradiance (W/m²)_camborne","Cloud Cover (%)_camborne",
    "Temperature (°C)_ashford","Precipitation (mm)_ashford",
    "Wind Speed (m/s)_ashford","Wind Direction (deg)_ashford",
    "Solar Irradiance (W/m²)_ashford","Cloud Cover (%)_ashford",
    "Temperature (°C)_eastanglia","Precipitation (mm)_eastanglia",
    "Wind Speed (m/s)_eastanglia","Wind Direction (deg)_eastanglia",
    "Solar Irradiance (W/m²)_eastanglia","Cloud Cover (%)_eastanglia",
    "Day","Holiday",
    "ND","TSD",
    "SETTLEMENT_DATE","SETTLEMENT_PERIOD",
    "Datetime_utc"
]

# Make sure all columns exist (some weather cols may not exist for older years)
for c in final_cols:
    if c not in master.columns:
        master[c] = np.nan

master = master[final_cols]

# ---------------- 8) Preview ----------------
display(master.head(4))
print("Rows:", len(master))
print("Cols:", len(master.columns))
print("Missing ND:", master['ND'].isna().sum())
print("Missing TSD:", master['TSD'].isna().sum())

# ---------------- 9) Save ----------------
out_path = f"GB_master_{year}.csv"
master.to_csv(out_path, index=False, encoding="utf-8-sig")
files.download(out_path)
print("\n✅ File saved:", out_path)