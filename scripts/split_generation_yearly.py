# ENVIRONMENT SETUP


def get_base_path():
    if os.path.exists("/content/drive"):
        return Path("/content/drive/MyDrive/uk-energy-grid")
    return Path(".")

BASE_PATH = get_base_path()

INPUT_PATH = BASE_PATH / "data" / "raw" / "Generation"/ "generation_full.csv"
OUTPUT_DIR = BASE_PATH / "data" / "processed" / "generation_by_year"


START_DATE = "2020-01-01"
END_DATE_EXCLUSIVE = "2025-10-01"



# FUNCTIONS


def load_data(file_path: Path) -> pd.DataFrame:
    if not file_path.exists():
        raise FileNotFoundError(f"Dataset not found: {file_path}")
    return pd.read_csv(file_path)


def preprocess_data(df: pd.DataFrame) -> pd.DataFrame:
    if "DATETIME" not in df.columns:
        raise ValueError("Missing required column: DATETIME")

    df["DATETIME"] = pd.to_datetime(df["DATETIME"], errors="coerce")
    df = df.dropna(subset=["DATETIME"])

    mask = (df["DATETIME"] >= START_DATE) & (df["DATETIME"] < END_DATE_EXCLUSIVE)
    df = df.loc[mask].copy()

    df["YEAR"] = df["DATETIME"].dt.year
    return df


def save_yearly_files(df: pd.DataFrame, output_dir: Path) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)

    for year, group in df.groupby("YEAR"):
        output_file = output_dir / f"generation_{year}.csv"
        group.drop(columns=["YEAR"]).to_csv(output_file, index=False)
        print(f"Saved: {output_file}")



# MAIN


def main():
    print(f"Base path: {BASE_PATH}")
    print(f"Input file: {INPUT_PATH}")

    df = load_data(INPUT_PATH)

    print("Processing dataset...")
    df_processed = preprocess_data(df)

    print("Saving yearly files...")
    save_yearly_files(df_processed, OUTPUT_DIR)

    print("Completed successfully.")


if __name__ == "__main__":
    main()