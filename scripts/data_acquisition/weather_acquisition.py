import os
import requests
import pandas as pd
from datetime import date
import holidays
import pytz

OUTPUT_PATH = "data/raw/weather/uk_weather_wide.csv"

cities = {
    "Aberdeen": {"lat": 57.150, "lon": -2.094},
    "Humber": {"lat": 53.768, "lon": -0.327},
    "Camborne": {"lat": 50.213, "lon": -5.297},
    "Ashford": {"lat": 51.148, "lon": 0.873},
    "EastAnglia": {"lat": 52.205, "lon": 0.119}
}

start_date = date(2020, 1, 1)
end_date = date(2025, 9, 30)

params = {
    "hourly": [
        "temperature_2m",
        "precipitation",
        "windspeed_10m",
        "winddirection_10m",
        "shortwave_radiation",
        "cloudcover"
    ],
    "start_date": start_date.strftime("%Y-%m-%d"),
    "end_date": end_date.strftime("%Y-%m-%d"),
    "timezone": "GMT"
}


def fetch_city_data(city_name, lat, lon):
    url = "https://archive-api.open-meteo.com/v1/archive"
    response = requests.get(url, params={**params, "latitude": lat, "longitude": lon})
    response.raise_for_status()
    data = response.json()

    if "hourly" not in data:
        raise ValueError(f"No data for {city_name}")

    df = pd.DataFrame(data["hourly"])

    df.rename(columns={
        "temperature_2m": "Temperature (°C)",
        "precipitation": "Precipitation (mm)",
        "windspeed_10m": "Wind Speed (m/s)",
        "winddirection_10m": "Wind Direction (deg)",
        "shortwave_radiation": "Solar Irradiance (W/m²)",
        "cloudcover": "Cloud Cover (%)",
        "time": "Datetime"
    }, inplace=True)

    df["Datetime"] = pd.to_datetime(df["Datetime"], utc=True)

    suffix = "_" + city_name.lower()
    cols = [c for c in df.columns if c != "Datetime"]

    df.rename(columns={c: f"{c}{suffix}" for c in cols}, inplace=True)

    keep_cols = ["Datetime"] + [c for c in df.columns if c.endswith(suffix)]
    return df[keep_cols].sort_values("Datetime")


def merge_all_cities():
    merged = None

    for city, coords in cities.items():
        df = fetch_city_data(city, coords["lat"], coords["lon"])
        merged = df if merged is None else merged.merge(df, on="Datetime", how="outer")

    return merged.sort_values("Datetime")


def add_calendar_features(df):
    df["Datetime"] = pd.to_datetime(df["Datetime"], utc=True)

    uk_tz = pytz.timezone("Europe/London")
    dt_local = df["Datetime"].dt.tz_convert(uk_tz)

    df["Day"] = dt_local.dt.day_name()

    years = range(dt_local.dt.year.min(), dt_local.dt.year.max() + 1)

    hol_eng = holidays.UnitedKingdom(years=years, subdiv="England")
    hol_wal = holidays.UnitedKingdom(years=years, subdiv="Wales")
    hol_sco = holidays.UnitedKingdom(years=years, subdiv="Scotland")

    gb_holidays = set(hol_eng) | set(hol_wal) | set(hol_sco)

    local_dates = dt_local.dt.date
    df["Holiday"] = ["Yes" if d in gb_holidays else "No" for d in local_dates]

    return df


def main():
    os.makedirs(os.path.dirname(OUTPUT_PATH), exist_ok=True)

    print("Fetching weather data...")
    df = merge_all_cities()

    print("Adding calendar features...")
    df = add_calendar_features(df)

    df.to_csv(OUTPUT_PATH, index=False, encoding="utf-8-sig")

    print(f"Saved: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()