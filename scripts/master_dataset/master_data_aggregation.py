# PATH CONFIGURATION


def get_base_path():
    if os.path.exists("/content/drive"):
        return Path("/content/drive/MyDrive/uk-energy-grid")
    return Path(".")

BASE_PATH = get_base_path()

INPUT_DIR = BASE_PATH / "data" / "processed" / "yearly_master_data"
OUTPUT_DIR = BASE_PATH / "data" / "processed" / "final_dataset"

OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# LOAD DATA


def load_yearly_files():

    files = sorted(INPUT_DIR.glob("GB_master_*.csv"))

    if not files:
        raise FileNotFoundError("No yearly master files found")

    dfs = []

    for f in files:
        print(f"Reading: {f.name}")

        df = pd.read_csv(
            f,
            parse_dates=["Datetime_utc"],
            low_memory=False
        )

        dfs.append(df)

    return dfs


# COMBINE DATA


def combine_data(dfs):

    combined = pd.concat(dfs, ignore_index=True)

    combined = combined.sort_values("Datetime_utc").reset_index(drop=True)

    print("\nCombined rows:", len(combined))
    print("Time range:",
          combined["Datetime_utc"].min(),
          "→",
          combined["Datetime_utc"].max())

    return combined


# DUPLICATE CHECK


def check_duplicates(df):

    dup_mask = df.duplicated(subset=["Datetime_utc"], keep=False)
    duplicates = df.loc[dup_mask].copy()

    print("\nDuplicate timestamps:",
          duplicates["Datetime_utc"].nunique(),
          "(rows:", len(duplicates), ")")

    return duplicates


# MISSING TIMESTAMP CHECK


def check_missing_timestamps(df):

    start = df["Datetime_utc"].min()
    end   = df["Datetime_utc"].max()

    expected = pd.date_range(start=start, end=end, freq="30min")

    missing = expected.difference(df["Datetime_utc"])

    print("Missing 30-min timestamps:", len(missing))

    return pd.DataFrame({"Datetime_utc": missing})

# SAVE OUTPUTS


def save_outputs(combined, missing_df, duplicates):

    master_path = OUTPUT_DIR / "GB_master_2020_2025.csv"
    combined.to_csv(master_path, index=False)

    print(f"\nSaved master dataset: {master_path}")

    missing_path = OUTPUT_DIR / "missing_timestamps.csv"
    missing_df.to_csv(missing_path, index=False)

    print(f"Saved missing timestamps: {missing_path}")

    if len(duplicates) > 0:
        dup_path = OUTPUT_DIR / "duplicate_timestamps.csv"
        duplicates.to_csv(dup_path, index=False)
        print(f"Saved duplicate timestamps: {dup_path}")
    else:
        print("No duplicate timestamps detected.")


# MAIN PIPELINE

def main():

    print("Base path:", BASE_PATH)
    print("Input directory:", INPUT_DIR)

    dfs = load_yearly_files()

    combined = combine_data(dfs)

    duplicates = check_duplicates(combined)

    missing_df = check_missing_timestamps(combined)

    save_outputs(combined, missing_df, duplicates)

    print("\nPipeline completed successfully.")


# RUN


if __name__ == "__main__":
    main()