# PATH CONFIGURATION

def get_base_path():
    if os.path.exists("/content/drive"):
        return Path("/content/drive/MyDrive/uk-energy-grid")
    return Path(".")

BASE_PATH = get_base_path()

GEN_DIR = BASE_PATH / "data" / "processed" / "generation_with_frequency"
WEATHER_FILE = BASE_PATH / "data" / "raw" / "Weather" / "uk_weather_from2020_WIDE.csv"
OUTPUT_DIR = BASE_PATH / "data" / "processed" / "generation_with_frequency_and_weather"

OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# LOAD WEATHER DATA


def load_weather():
    if not WEATHER_FILE.exists():
        raise FileNotFoundError(f"Weather file not found: {WEATHER_FILE}")

    df = pd.read_csv(WEATHER_FILE, low_memory=False)

    if "Datetime" not in df.columns:
        raise ValueError("Weather file must contain 'Datetime' column")

    df["Datetime"] = pd.to_datetime(df["Datetime"], utc=True, errors="coerce")
    df = df.dropna(subset=["Datetime"]).sort_values("Datetime")

    print("Weather rows loaded:", len(df))

    return df


# ALIGN WEATHER TO 30-MIN


def align_weather_to_30min(wx_df, target_index):

    wx_df = wx_df.drop_duplicates(subset=["Datetime"]).set_index("Datetime")

    # Upsample hourly → 30-min
    wx_30 = wx_df.resample("30min").ffill()

    # Force full calendar alignment
    wx_30 = wx_30.reindex(target_index).ffill().bfill()

    return wx_30.reset_index()


# PROCESS YEAR


def process_year(gen_file: Path, weather_df: pd.DataFrame):

    print("\n============================")
    print(f"Processing: {gen_file.name}")
    print("============================")

    df = pd.read_csv(gen_file)

    if "DATETIME" not in df.columns:
        raise ValueError(f"{gen_file.name} missing DATETIME")

    df["DATETIME"] = pd.to_datetime(df["DATETIME"], utc=True, errors="coerce")
    df = df.sort_values("DATETIME")

    # Extract year
    year = df["DATETIME"].dt.year.iloc[0]

    start_date = pd.Timestamp(f"{year}-01-01", tz="UTC")
    end_date   = pd.Timestamp(f"{year+1}-01-01", tz="UTC")

    df_year = df[(df["DATETIME"] >= start_date) & (df["DATETIME"] < end_date)].copy()

    print(f"Rows (30-min): {len(df_year)}")

    # Build target index
    full_index = pd.DatetimeIndex(df_year["DATETIME"].unique(), name="Datetime")

    # Filter weather
    wx_year = weather_df[
        (weather_df["Datetime"] >= start_date) &
        (weather_df["Datetime"] < end_date)
    ].copy()

    print(f"Weather rows (hourly): {len(wx_year)}")

    # Align weather
    wx_30 = align_weather_to_30min(wx_year, full_index)

    # Merge
    merged = df_year.merge(
        wx_30,
        left_on="DATETIME",
        right_on="Datetime",
        how="left"
    ).drop(columns=["Datetime"])

    print("Merged shape:", merged.shape)

    return merged, year


# QA CHECKS


def run_quality_checks(df, year):

    print("\n[QA CHECKS]")

    expected_last = pd.Timestamp(f"{year}-12-31 23:30:00", tz="UTC")

    print("Row count:", len(df))
    print("Last timestamp:", df["DATETIME"].iloc[-1])
    print("Correct last timestamp:", df["DATETIME"].iloc[-1] == expected_last)

    print("NaNs in last row:", df.iloc[-1].isna().sum())


# MAIN PIPELINE


def main():

    print("Base path:", BASE_PATH)

    weather_df = load_weather()

    gen_files = sorted(GEN_DIR.glob("generation_freq_*.csv"))

    if not gen_files:
        raise FileNotFoundError("No generation_with_frequency files found")

    for gen_file in gen_files:

        try:
            merged, year = process_year(gen_file, weather_df)

            output_file = OUTPUT_DIR / f"master_{year}.csv"
            merged.to_csv(output_file, index=False)

            print(f"Saved: {output_file}")

            run_quality_checks(merged, year)

        except Exception as e:
            print(f" Error processing {gen_file.name}: {e}")


# RUN


if __name__ == "__main__":
    main()