BASE_PATH = get_base_path()

GEN_DIR    = BASE_PATH / "data" / "processed" / "generation_by_year"
FREQ_DIR   = BASE_PATH / "data" / "raw" / "Frequency"
OUTPUT_DIR = BASE_PATH / "data" / "processed" / "generation_with_frequency"

OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# FREQUENCY FILE READER


def read_csv_file(file_obj):
    df = pd.read_csv(file_obj)

    if not {"dtm", "f"}.issubset(df.columns):
        raise ValueError("Frequency file must contain 'dtm' and 'f' columns")

    df["dtm"] = pd.to_datetime(df["dtm"], errors="coerce")
    df = df.dropna(subset=["dtm", "f"])

    return df[["dtm", "f"]]


# LOAD YEARLY FREQUENCY DATA


def load_frequency_year(year_path: Path) -> pd.DataFrame:
    files = list(year_path.glob("*.csv")) + list(year_path.glob("*.zip"))

    if not files:
        raise FileNotFoundError(f"No frequency files found in {year_path}")

    dfs = []

    for f in files:

        if f.suffix.lower() == ".csv":
            print(f"Reading CSV: {f.name}")
            df = read_csv_file(f)
            dfs.append(df)

        elif f.suffix.lower() == ".zip":
            print(f"Reading ZIP: {f.name}")
            with zipfile.ZipFile(f, "r") as z:
                for name in z.namelist():
                    if name.endswith(".csv"):
                        with z.open(name) as file:
                            df = read_csv_file(file)
                            dfs.append(df)

        else:
            print(f"Skipping unsupported file: {f.name}")

    freq = pd.concat(dfs, ignore_index=True)
    freq = freq.drop_duplicates(subset=["dtm"]).sort_values("dtm")

    print(f"Total frequency rows loaded: {len(freq)}")

    return freq


# AGGREGATE TO 30-MIN LEVEL


def aggregate_frequency(freq: pd.DataFrame) -> pd.DataFrame:

    freq["bin_start"] = freq["dtm"].dt.floor("30min")

    agg = (
        freq.groupby("bin_start")
        .agg(
            freq_mean=("f", "mean"),
            freq_min=("f", "min"),
            freq_max=("f", "max"),
            freq_std=("f", "std"),
            out_of_49_5_50_5=("f", lambda s: np.sum((s < 49.5) | (s > 50.5))),
            out_of_49_8_50_2=("f", lambda s: np.sum((s < 49.8) | (s > 50.2))),
            n_points=("f", "size"),
        )
        .reset_index()
    )

    print("Aggregated frequency rows:", len(agg))

    return ag




# MERGE WITH GENERATION DATA




def merge_generation_with_frequency(gen_file: Path, freq_agg: pd.DataFrame):

    gen = pd.read_csv(gen_file)

    if "DATETIME" not in gen.columns:
        raise ValueError(f"{gen_file.name} missing DATETIME column")

    gen["_DT_KEY"] = pd.to_datetime(gen["DATETIME"], errors="coerce")
    freq_agg["_DT_KEY"] = freq_agg["bin_start"]

    merged = pd.merge(
        gen,
        freq_agg.drop(columns=["bin_start"]),
        on="_DT_KEY",
        how="left"
    )

    merged = merged.drop(columns=["_DT_KEY"])

    print("Merged dataset shape:", merged.shape)

    return merged


# MAIN PIPELINE


def main():

    print("Base path:", BASE_PATH)
    print("Generation directory:", GEN_DIR)
    print("Frequency directory:", FREQ_DIR)

    generation_files = sorted(GEN_DIR.glob("generation_*.csv"))

    if not generation_files:
        raise FileNotFoundError("No generation files found")

    for gen_file in generation_files:

        year = gen_file.stem.split("_")[-1]

        print("\n============================")
        print(f"Processing year: {year}")
        print("============================")

        freq_year_path = FREQ_DIR / year

        if not freq_year_path.exists():
            print(f" Skipping {year}: folder not found")
            continue

        files = list(freq_year_path.glob("*"))

        print(f"Frequency files found: {len(files)}")

        if len(files) == 0:
            print(f" Skipping {year}: empty folder")
            continue

        try:
            freq = load_frequency_year(freq_year_path)
            freq_agg = aggregate_frequency(freq)

            merged = merge_generation_with_frequency(gen_file, freq_agg)

            output_file = OUTPUT_DIR / f"generation_freq_{year}.csv"
            merged.to_csv(output_file, index=False)

            print(f"Saved: {output_file}")

        except Exception as e:
            print(f" Error processing {year}: {e}")


# RUN


if __name__ == "__main__":
    main()