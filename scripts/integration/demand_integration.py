BASE_PATH = get_base_path()

GEN_DIR = BASE_PATH / "data" / "processed" / "generation_with_frequency_and_weather"
DEM_DIR = BASE_PATH / "data" / "raw" / "Demand"
OUTPUT_DIR = BASE_PATH / "data" / "processed" / "yearly_master_data"

OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


# DEMAND LOADER


def load_demand(year: int) -> pd.DataFrame:

    file_path = DEM_DIR / f"demanddata_{year}.csv"

    if not file_path.exists():
        raise FileNotFoundError(f"Demand file not found: {file_path}")

    df = pd.read_csv(file_path)

    # Convert settlement date format
    df["SETTLEMENT_DATE"] = pd.to_datetime(
        df["SETTLEMENT_DATE"],
        dayfirst=False,
        errors="coerce"
    ).dt.strftime('%d-%b-%y')

    df["SETTLEMENT_PERIOD"] = pd.to_numeric(
        df["SETTLEMENT_PERIOD"],
        errors="coerce"
    ).astype("Int64").astype(int)

    df = df[["SETTLEMENT_DATE", "SETTLEMENT_PERIOD", "ND", "TSD"]]

    print(f"Demand rows loaded ({year}): {len(df)}")

    return df


# GENERATION PROCESSING (SETTLEMENT KEYS)


def prepare_generation(df: pd.DataFrame) -> pd.DataFrame:

    df["Datetime_utc"] = pd.to_datetime(df["DATETIME"], utc=True, errors="coerce")
    df = df.sort_values("Datetime_utc").reset_index(drop=True)

    df["Datetime_local"] = df["Datetime_utc"].dt.tz_convert("Europe/London")

    df["SETTLEMENT_DATE"] = df["Datetime_local"].dt.strftime('%d-%b-%y')

    df = df.sort_values("Datetime_local")

    df["SETTLEMENT_PERIOD"] = (
        df.groupby(df["Datetime_local"].dt.date).cumcount() + 1
    ).astype(int)

    return df


# MERGE FUNCTION


def merge_demand(gen_df: pd.DataFrame, dem_df: pd.DataFrame) -> pd.DataFrame:

    merged = gen_df.merge(
        dem_df,
        on=["SETTLEMENT_DATE", "SETTLEMENT_PERIOD"],
        how="left"
    )

    print("Merged shape:", merged.shape)

    return merged


# QUALITY CHECKS


def run_quality_checks(df, year):

    print("\n[QA CHECKS]")

    print("Rows:", len(df))
    print("Missing ND:", df["ND"].isna().sum())
    print("Missing TSD:", df["TSD"].isna().sum())

    expected_last = pd.Timestamp(f"{year}-12-31 23:30:00", tz="UTC")
    print("Last timestamp correct:",
          df["Datetime_utc"].iloc[-1] == expected_last)


# MAIN PIPELINE


def main():

    print("Base path:", BASE_PATH)

    gen_files = sorted(GEN_DIR.glob("*.csv"))

    if not gen_files:
        raise FileNotFoundError("No generation files found")

    for gen_file in gen_files:

        print("\n============================")
        print(f"Processing: {gen_file.name}")
        print("============================")

        try:
            df = pd.read_csv(gen_file)

            if "DATETIME" not in df.columns:
                raise ValueError(f"{gen_file.name} missing DATETIME")

            # Extract year
            year = pd.to_datetime(df["DATETIME"]).dt.year.iloc[0]

            df = prepare_generation(df)

            dem_df = load_demand(year)

            merged = merge_demand(df, dem_df)

            output_file = OUTPUT_DIR / f"GB_master_{year}.csv"
            merged.to_csv(output_file, index=False)

            print(f"Saved: {output_file}")

            run_quality_checks(merged, year)

        except Exception as e:
            print(f" Error processing {gen_file.name}: {e}")


# RUN


if __name__ == "__main__":
    main()